# ფუნქციები კოდს ხდის უფრო ორგანიზებულს და გასაგებს.
# საშუალებას გვაძლევს თავიდან ავიცილოთ კოდის გამეორება (DRY პრინციპი – Don't Repeat Yourself).
# თუ რაიმე შეცვლაა საჭირო, საკმარისია ცვლილება მხოლოდ ფუნქციაში — რაც ამცირებს შეცდომების ალბათობას.
# ფუნქციები აადვილებს კოდის ტესტირებასა და შენარჩუნებას.
# რთული პრობლემები შეიძლება დავყოთ პატარა, მართვად ბლოკებად.
# შეგვიძლია ერთსა და იმავე ფუნქციას გამოვუწოდოთ სხვადასხვა მონაცემი და მივიღოთ სხვადასხვა შედეგი.
# ფუნქციები ხელს უწყობს კოდის ხელახალ გამოყენებას სხვა პროექტებშიც.